-- 1. GARANTIR QUE AS TABELAS EXISTAM

-- Tabela de Configurações (White Label)
CREATE TABLE IF NOT EXISTS public.configuracoes (
    key TEXT PRIMARY KEY,
    value TEXT,
    description TEXT,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);
ALTER TABLE public.configuracoes ENABLE ROW LEVEL SECURITY;
-- Permitir leitura pública ou autenticada (para carregar tema no login)
CREATE POLICY "Leitura de Configurações" ON public.configuracoes FOR SELECT USING (true);
-- Permitir escrita apenas para autenticados (ou admins, mas aqui deixamos autenticados para facilitar)
CREATE POLICY "Escrita de Configurações" ON public.configuracoes FOR ALL USING (auth.role() = 'authenticated');


-- Tabela de Clientes (Base) - Caso não exista
CREATE TABLE IF NOT EXISTS public.clientes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome_empresa TEXT,
    nome_fantasia TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- Tabela de Perfis (Profiles) - Espelho de Auth.Users
CREATE TABLE IF NOT EXISTS public.profiles (
    id UUID REFERENCES auth.users(id) ON DELETE CASCADE PRIMARY KEY,
    email TEXT,
    nome TEXT,
    avatar_url TEXT,
    role TEXT DEFAULT 'user',
    tenant_id BIGINT REFERENCES public.clientes(id),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);
ALTER TABLE public.profiles ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Public Profiles are viewable by everyone" ON public.profiles FOR SELECT USING (true);
CREATE POLICY "Users can insert their own profile" ON public.profiles FOR INSERT WITH CHECK (auth.uid() = id);
CREATE POLICY "Users can update own profile" ON public.profiles FOR UPDATE USING (auth.uid() = id);

-- Trigger para criar Profile automaticamente ao criar User
CREATE OR REPLACE FUNCTION public.handle_new_user() 
RETURNS TRIGGER AS $$
BEGIN
  INSERT INTO public.profiles (id, email, nome, tenant_id)
  VALUES (
    new.id,
    new.email,
    new.raw_user_meta_data->>'full_name',
    CASE
      WHEN (new.raw_user_meta_data->>'tenant_id') ~ '^\d+$' THEN (new.raw_user_meta_data->>'tenant_id')::bigint
      WHEN (new.raw_user_meta_data->>'cliente_id') ~ '^\d+$' THEN (new.raw_user_meta_data->>'cliente_id')::bigint
      ELSE NULL
    END
  );
  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE PROCEDURE public.handle_new_user();

-- Tabela de Lembretes/Notificações
CREATE TABLE IF NOT EXISTS public.lembretes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    titulo TEXT NOT NULL,
    concluido BOOLEAN DEFAULT false,
    user_id UUID REFERENCES auth.users(id), -- NULL = Público/Todos
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- Tabela de Tarefas
CREATE TABLE IF NOT EXISTS public.tarefas (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    titulo TEXT NOT NULL,
    descricao TEXT,
    status TEXT DEFAULT 'pendente',
    prazo_tipo TEXT CHECK (prazo_tipo IN ('para_dia', 'ate_dia')),
    prazo_data TIMESTAMP WITH TIME ZONE,
    tipo TEXT DEFAULT 'tarefa' CHECK (tipo IN ('tarefa', 'reuniao')),
    horario_reuniao TIME,
    criado_por UUID REFERENCES auth.users(id),
    cliente_id BIGINT REFERENCES public.clientes(id),
    time_id UUID REFERENCES public.times(id),
    post_id UUID REFERENCES public.social_posts(id), -- Link com Social Media
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- Tabela de Atribuições (Quem faz a tarefa)
CREATE TABLE IF NOT EXISTS public.tarefa_atribuicoes (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    tarefa_id UUID REFERENCES public.tarefas(id) ON DELETE CASCADE,
    usuario_email TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- Tabela de Histórico da Tarefa
CREATE TABLE IF NOT EXISTS public.tarefa_historico (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    tarefa_id UUID REFERENCES public.tarefas(id) ON DELETE CASCADE,
    usuario_id UUID REFERENCES auth.users(id),
    tipo TEXT NOT NULL,
    descricao TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 2. ATUALIZAR COLUNAS (Caso as tabelas já existam mas faltem colunas)

DO $$
BEGIN
    -- Adicionar post_id em tarefas se não existir
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='tarefas' AND column_name='post_id') THEN
        ALTER TABLE public.tarefas ADD COLUMN post_id UUID REFERENCES public.social_posts(id);
    END IF;

    -- Adicionar user_id em lembretes se não existir
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='lembretes' AND column_name='user_id') THEN
        ALTER TABLE public.lembretes ADD COLUMN user_id UUID REFERENCES auth.users(id);
    END IF;
END $$;

