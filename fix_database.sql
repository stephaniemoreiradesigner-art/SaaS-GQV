-- 4. AUTOMAÇÃO E TRIGGERS

-- Trigger 1: Criar Tarefa de Ajuste quando Cliente pede ajuste no Post
CREATE OR REPLACE FUNCTION public.handle_post_adjustment_trigger()
RETURNS TRIGGER AS $$
DECLARE
    v_cliente_nome TEXT;
    v_tarefa_id UUID;
BEGIN
    IF NEW.status = 'ajuste_solicitado' AND OLD.status != 'ajuste_solicitado' THEN
        
        -- Tentar pegar nome do cliente
        SELECT COALESCE(nome_empresa, nome_fantasia, 'Cliente') INTO v_cliente_nome FROM public.clientes WHERE id = NEW.cliente_id;
        
        -- Criar Tarefa
        INSERT INTO public.tarefas (
            titulo,
            descricao,
            status,
            prazo_data,
            tipo,
            cliente_id,
            criado_por,
            post_id
        ) VALUES (
            'Ajuste em Post: ' || NEW.tema,
            'O cliente solicitou ajustes no post de ' || to_char(NEW.data_agendada, 'DD/MM/YYYY') || '. Feedback: ' || COALESCE(NEW.feedback_cliente->>'comentario', 'Ver detalhes no post'),
            'pendente',
            NOW() + INTERVAL '2 days',
            'tarefa',
            NEW.cliente_id,
            NULL, -- Sistema
            NEW.id
        ) RETURNING id INTO v_tarefa_id;

        -- Criar Lembrete Geral
        INSERT INTO public.lembretes (titulo, concluido, user_id) 
        VALUES ('⚠️ Ajuste solicitado pelo cliente ' || COALESCE(v_cliente_nome, 'Desconhecido') || ' no post "' || NEW.tema || '"', false, NULL);
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS trigger_auto_create_adjustment_task ON social_posts;
CREATE TRIGGER trigger_auto_create_adjustment_task
AFTER UPDATE ON social_posts
FOR EACH ROW
EXECUTE FUNCTION public.handle_post_adjustment_trigger();


-- Trigger 2: Sincronizar Status Tarefa -> Post (Em Andamento -> Ajuste em Andamento)
CREATE OR REPLACE FUNCTION public.sync_task_to_post_status()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.post_id IS NOT NULL AND NEW.status = 'em_andamento' AND OLD.status != 'em_andamento' THEN
        UPDATE public.social_posts 
        SET status = 'ajuste_em_andamento' 
        WHERE id = NEW.post_id;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS trigger_sync_task_to_post ON public.tarefas;
CREATE TRIGGER trigger_sync_task_to_post
AFTER UPDATE ON public.tarefas
FOR EACH ROW
EXECUTE FUNCTION public.sync_task_to_post_status();


-- Trigger 3: Sincronizar Status Post -> Tarefa (Salvar Ajuste/Pendente -> Concluir Tarefa)
CREATE OR REPLACE FUNCTION public.sync_post_to_task_status()
RETURNS TRIGGER AS $$
BEGIN
    -- Se o post voltou para pendente (re-aprovação) vindo de um estado de ajuste
    IF NEW.status = 'pendente' AND OLD.status IN ('ajuste_solicitado', 'ajuste_em_andamento') THEN
        UPDATE public.tarefas 
        SET status = 'concluido' 
        WHERE post_id = NEW.id AND status != 'concluido';
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS trigger_sync_post_to_task ON public.social_posts;
CREATE TRIGGER trigger_sync_post_to_task
AFTER UPDATE ON public.social_posts
FOR EACH ROW
EXECUTE FUNCTION public.sync_post_to_task_status();


-- Trigger 4: Notificar Criador quando Status da Tarefa muda
CREATE OR REPLACE FUNCTION public.notify_task_creator_on_status_change()
RETURNS TRIGGER AS $$
DECLARE
    v_criador_id UUID;
    v_msg TEXT;
BEGIN
    -- Se status mudou E tem criador definido
    IF NEW.status IS DISTINCT FROM OLD.status AND NEW.criado_por IS NOT NULL THEN
        v_criador_id := NEW.criado_por;
        
        IF NEW.status = 'em_andamento' THEN
            v_msg := '🚀 A tarefa "' || NEW.titulo || '" iniciou o andamento.';
        ELSIF NEW.status IN ('concluido', 'concluida') THEN
            v_msg := '✅ A tarefa "' || NEW.titulo || '" foi concluída.';
        ELSIF NEW.status = 'solicitacao_prazo' THEN
             v_msg := '⏳ Solicitação de prazo na tarefa "' || NEW.titulo || '".';
        ELSE
            RETURN NEW;
        END IF;

        INSERT INTO public.lembretes (titulo, concluido, user_id) 
        VALUES (v_msg, false, v_criador_id);
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS trigger_notify_task_creator ON tarefas;
CREATE TRIGGER trigger_notify_task_creator
AFTER UPDATE ON tarefas
FOR EACH ROW
EXECUTE FUNCTION public.notify_task_creator_on_status_change();
